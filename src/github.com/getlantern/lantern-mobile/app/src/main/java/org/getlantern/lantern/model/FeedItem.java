package org.getlantern.lantern.model;

public class FeedItem {
    public String Title;
    public String Description;
    public String Image;
    public String Url;

    public FeedItem(String mTitle, String mDesc, String mImage, String mUrl) {
        Title = mTitle;
        Description = mDesc;
        Image = mImage;
        Url = mUrl;
    }
}
 
